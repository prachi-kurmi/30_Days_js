export function add(num1, num2) {
    return (`Num 1: ${num1} addition with Num2: ${num2}: ${num1+num2}`) ;
}

export function sub(num1, num2) {
    return (`Num 1: ${num1} subtraction with Num2: ${num2}: ${num1-num2}`) ;
}

export function multiply(num1, num2) {
    return (`Num 1: ${num1} multiplication with Num2: ${num2}: ${num1*num2}`) ;
}
